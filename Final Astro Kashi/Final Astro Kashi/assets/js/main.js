function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  updateLanguage();
  closeLangModal();
}

function updateLanguage() {
  const t = translations[currentLang] || translations.en;
  
  // Update lang switcher
  const langFlag = document.getElementById('lang-flag');
  const langText = document.getElementById('lang-text');
  if (langFlag && langText) {
    langFlag.textContent = currentLang === 'hi' ? '🇮🇳' : '🇺🇸';
    langText.textContent = currentLang === 'hi' ? 'HI' : 'EN';
  }

  // Update all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    if (t[key]) {
      el.innerHTML = t[key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
    const key = el.getAttribute('data-translate-placeholder');
    if (t[key]) {
      el.placeholder = t[key];
    }
  });

  // Update options
  document.querySelectorAll('[data-translate-option]').forEach(el => {
    const key = el.getAttribute('data-translate-option');
    if (t[key]) {
      el.textContent = t[key];
    }
  });

  // Update countdown labels if they exist
  const countdownEl = document.getElementById('countdown');
  if (countdownEl) {
    updateCountdown();
  }
}

