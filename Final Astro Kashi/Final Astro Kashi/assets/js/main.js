// BOOKING FORMS -> AJAX + WhatsApp
// Auto-wires every "Proceed to Payment / Book Now" button inside a
// .pricing-card on any puja page. Reads the selected plan + all form
// fields, sends them to the backend via AJAX, then opens WhatsApp.
// ============================================================
(function () {
  var WHATSAPP_NUMBER = '9140793265'; // business WhatsApp number
  var ENDPOINT = 'http://shivakashitravels.com/home/pujacontent';

  function bookingHandler(card, btn) {
    return function (e) {
      e.preventDefault();

      // Validate all fields inline before doing anything else
      if (!KPForm.validate(card)) return;

      // Service name (from the card heading, e.g. "Book Rudrabhishek Puja",
      // or the contact card title "Send Us a Message")
      var headEl = card.querySelector('.pricing-header h3') || card.querySelector('.booking-card-title');
      var service = headEl ? headEl.textContent.replace(/^Book\s+/i, '').trim() : document.title;

      // Selected plan + price
      var planEl = card.querySelector('.plan-option.selected');
      var plan = planEl && planEl.querySelector('.plan-name') ? planEl.querySelector('.plan-name').textContent.trim() : '';
      var price = planEl && planEl.querySelector('.plan-price') ? planEl.querySelector('.plan-price').textContent.trim() : '';

      // Collect every label -> value pair from the form fields
      var fields = [];
      var phone = '';
      card.querySelectorAll('.form-group').forEach(function (grp) {
        var labelEl = grp.querySelector('.form-label');
        var inputEl = grp.querySelector('input, select, textarea');
        if (!inputEl) return;
        var label = labelEl ? labelEl.textContent.trim() : 'Field';
        var value = (inputEl.value || '').trim();
        if (inputEl.type === 'tel') phone = value;
        if (value) fields.push({ label: label, value: value });
      });

      // 1) Fire AJAX to backend (does not block the WhatsApp redirect)
      try {
        var fd = new FormData();
        fd.append('service', service);
        fd.append('package', plan);
        fd.append('price', price);
        fd.append('phone', phone);
        fd.append('source', 'Shri Kashi Astro - ' + (location.pathname.split('/').pop() || ''));
        fields.forEach(function (f) { fd.append(f.label, f.value); });

        fetch(ENDPOINT, { method: 'POST', body: fd })
          .catch(function (err) { console.error('Lead submit failed:', err); });
      } catch (err) {
        console.error('Lead submit error:', err);
      }

      // 2) Open WhatsApp with a prefilled summary
      var lines = [
        'New Puja Booking Request',
        '------------------------',
        'Service: ' + service
      ];
      if (plan) lines.push('Package: ' + plan + (price ? ' (' + price + ')' : ''));
      fields.forEach(function (f) { lines.push(f.label + ': ' + f.value); });

      var msg = lines.map(encodeURIComponent).join('%0A');
      window.open('https://wa.me/' + WHATSAPP_NUMBER + '?text=' + msg, '_blank');
    };
  }

  document.querySelectorAll('.pricing-card a.btn-primary, .booking-card a.btn-primary').forEach(function (btn) {
    var card = btn.closest('.pricing-card, .booking-card');
    if (card) btn.addEventListener('click', bookingHandler(card, btn));
  });
})();

